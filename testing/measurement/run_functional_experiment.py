import json
import subprocess
import sys
import time
from pathlib import Path
from typing import Any
from urllib.error import HTTPError, URLError
from urllib.request import Request, urlopen

from run_load_experiment import (
    DEFAULT_LOCAL_COMPOSE_FILE,
    DEFAULT_PROMETHEUS_SCRAPE_JOBS,
    DEFAULT_RESULTS_ROOT,
    DEFAULT_TARGET_COMPOSE_FILE,
    DEFAULT_TARGET_DATA_DIR,
    DEFAULT_TARGET_DIR,
    DEFAULT_TARGET_HOST,
    DEFAULT_TARGET_USER,
    collect_target_diagnostics,
    command_text,
    host_for_url,
    iso_z,
    local_compose_command,
    path_from_project,
    remote_compose_command,
    remote_reset_data_command,
    run_command,
    sanitize_name,
    sleep_with_progress,
    ssh_destination,
    utc_now,
    validate_remote_data_dir,
    write_json,
)


DEFAULT_DEVICE_ID = 'smart-meter-001'
DEFAULT_DEVICE_SECRET = 'smart-meter-001_password'
DEFAULT_MEASUREMENT_UNIT = 'kWh'
DEFAULT_UNIT_PRICE_MSAT = 100000
DEFAULT_REPORTING_STRATEGY = 'interval'
DEFAULT_REPORTING_INTERVAL = 1
DEFAULT_HEARTBEAT_INTERVAL = 60
DEFAULT_AUTHORIZE_REQUEST_MSAT = 1000


class ProvisionError(RuntimeError):
    def __init__(self, message: str, *, retryable: bool) -> None:
        super().__init__(message)
        self.retryable = retryable


def write_functional_compose_override(
    path: Path,
    *,
    target_host: str,
    mqtt_tls_server_name: str,
    device_id: str,
    device_secret: str,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    quoted_host = json.dumps(target_host)
    quoted_tls_server_name = json.dumps(mqtt_tls_server_name)
    quoted_device_id = json.dumps(device_id)
    quoted_device_secret = json.dumps(device_secret)
    path.write_text(
        '\n'.join(
            [
                '# Generated by testing/measurement/run_functional_experiment.py',
                'services:',
                '  prometheus:',
                '    environment:',
                f'      PROMETHEUS_TARGET_HOST: {quoted_host}',
                f'      PROMETHEUS_SCRAPE_JOBS: "${{PROMETHEUS_SCRAPE_JOBS:-{DEFAULT_PROMETHEUS_SCRAPE_JOBS}}}"',
                '  httpdevices:',
                '    environment:',
                f'      MQTT_BROKER: {quoted_host}',
                f'      MQTT_TLS_SERVER_NAME: {quoted_tls_server_name}',
                '  smartmeter:',
                '    environment:',
                f'      DEVICE_ID: {quoted_device_id}',
                f'      DEVICE_SECRET: {quoted_device_secret}',
                f'      MQTT_BROKER: {quoted_host}',
                f'      MQTT_TLS_SERVER_NAME: {quoted_tls_server_name}',
                '',
            ]
        )
    )


def default_run_name(target_host: str) -> str:
    return f"{utc_now().strftime('%Y%m%dT%H%M%SZ')}_functional_{sanitize_name(target_host)}"


def join_url(base_url: str, path: str) -> str:
    return f"{base_url.rstrip('/')}/{path.lstrip('/')}"


def build_device_payload(args: argparse.Namespace) -> dict[str, Any]:
    return {
        'device_id': args.device_id,
        'device_secret': args.device_secret,
        'measurement_unit': args.measurement_unit,
        'unit_price_msat': args.unit_price_msat,
        'reporting_strategy': args.reporting_strategy,
        'reporting_interval': args.reporting_interval,
        'heartbeat_interval': args.heartbeat_interval,
        'authorize_request_msat': args.authorize_request_msat,
        'timestamp': args.device_timestamp or iso_z(utc_now()),
    }


def post_json(url: str, payload: dict[str, Any], *, timeout_seconds: int) -> tuple[int, str]:
    body = json.dumps(payload).encode('utf-8')
    request = Request(
        url,
        data=body,
        headers={'Content-Type': 'application/json'},
        method='POST',
    )
    try:
        with urlopen(request, timeout=timeout_seconds) as response:
            return response.status, response.read().decode('utf-8')
    except HTTPError as exc:
        response_body = exc.read().decode('utf-8', errors='replace')
        retryable = exc.code >= 500
        raise ProvisionError(
            f"POST {url} failed with HTTP {exc.code}: {response_body}",
            retryable=retryable,
        ) from exc
    except (TimeoutError, URLError, OSError) as exc:
        raise ProvisionError(f"POST {url} failed: {exc}", retryable=True) from exc


def provision_device(
    *,
    url: str,
    payload: dict[str, Any],
    response_path: Path,
    dry_run: bool,
    request_timeout_seconds: int,
    ready_timeout_seconds: int,
    retry_interval_seconds: int,
) -> dict[str, Any]:
    print(f"$ POST {url}")
    print(json.dumps(payload, indent=2))
    if dry_run:
        print(f"[dry-run] would provision device {payload['device_id']} at {url}")
        return {
            'url': url,
            'request': payload,
            'status_code': None,
            'response_body': None,
            'response_file': str(response_path),
            'attempts': 0,
            'dry_run': True,
        }

    deadline = time.monotonic() + ready_timeout_seconds
    attempts = 0
    last_error = ''

    while True:
        attempts += 1
        try:
            status_code, response_body = post_json(url, payload, timeout_seconds=request_timeout_seconds)
            response_path.parent.mkdir(parents=True, exist_ok=True)
            response_path.write_text(response_body)
            print(f"Provisioned {payload['device_id']} with HTTP {status_code}")
            return {
                'url': url,
                'request': payload,
                'status_code': status_code,
                'response_body': response_body,
                'response_file': str(response_path),
                'attempts': attempts,
                'dry_run': False,
            }
        except ProvisionError as exc:
            last_error = str(exc)
            if not exc.retryable or time.monotonic() >= deadline:
                raise
            print(f"Device API not ready yet ({last_error}); retrying in {retry_interval_seconds}s")
            time.sleep(retry_interval_seconds)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description='Restart edge/local stacks from scratch and provision one functional smart-meter device.',
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument('--target-host', default=DEFAULT_TARGET_HOST,
                        help='Target edge machine host/IP')
    parser.add_argument('--target-user', default=DEFAULT_TARGET_USER,
                        help='SSH user for the target edge machine; use empty string for raw host')
    parser.add_argument('--target-dir', default=DEFAULT_TARGET_DIR,
                        help='Project directory on the target edge machine')
    parser.add_argument('--target-compose-file', default=DEFAULT_TARGET_COMPOSE_FILE,
                        help='Compose file path relative to --target-dir')
    parser.add_argument('--target-data-dir', default=DEFAULT_TARGET_DATA_DIR,
                        help='Remote edge data directory to clear before starting; relative to --target-dir unless absolute')
    parser.add_argument('--target-stabilize-seconds', type=int, default=5,
                        help='Wait after restarting the target edge stack')
    parser.add_argument('--skip-target-restart', action='store_true',
                        help='Do not restart the remote target stack')
    parser.add_argument('--skip-target-data-reset', action='store_true',
                        help='Keep the remote target data directory when restarting the edge stack')
    parser.add_argument('--no-target-data-reset-sudo', action='store_true',
                        help='Do not use sudo for the remote target data directory removal')
    parser.add_argument('--pull-target-images', action='store_true',
                        help='Run docker compose pull on the target before starting the edge stack')

    parser.add_argument('--local-compose-file', default=DEFAULT_LOCAL_COMPOSE_FILE,
                        help='Local evaluation compose file path')
    parser.add_argument('--local-stabilize-seconds', type=int, default=5,
                        help='Wait after restarting the local evaluation stack')
    parser.add_argument('--skip-local-restart', action='store_true',
                        help='Do not restart the local evaluation stack')

    parser.add_argument('--api-port', type=int, default=8080,
                        help='Target API port used when --api-base-url is omitted')
    parser.add_argument('--api-base-url', default=None,
                        help='Full target API base URL; defaults to http://<target-host>:<api-port>')
    parser.add_argument('--devices-endpoint', default='/devices',
                        help='Device provisioning endpoint path under --api-base-url')
    parser.add_argument('--request-timeout-seconds', type=int, default=15,
                        help='HTTP request timeout for device provisioning')
    parser.add_argument('--api-ready-timeout-seconds', type=int, default=120,
                        help='How long to retry provisioning while the target API comes up')
    parser.add_argument('--api-retry-interval-seconds', type=int, default=5,
                        help='Delay between retryable device provisioning attempts')
    parser.add_argument('--mqtt-tls-server-name', default=None,
                        help='MQTT TLS server name for local evaluation services; defaults to --target-host')

    parser.add_argument('--device-id', default=DEFAULT_DEVICE_ID,
                        help='Device id to provision and pass to the smartmeter service')
    parser.add_argument('--device-secret', default=DEFAULT_DEVICE_SECRET,
                        help='Device secret to provision and pass to the smartmeter service')
    parser.add_argument('--measurement-unit', default=DEFAULT_MEASUREMENT_UNIT,
                        help='Device measurement unit')
    parser.add_argument('--unit-price-msat', type=int, default=DEFAULT_UNIT_PRICE_MSAT,
                        help='Device unit price in millisatoshis')
    parser.add_argument('--reporting-strategy', default=DEFAULT_REPORTING_STRATEGY,
                        choices=('interval', 'delta', 'total'),
                        help='Device reporting strategy')
    parser.add_argument('--reporting-interval', type=int, default=DEFAULT_REPORTING_INTERVAL,
                        help='Device reporting interval in seconds')
    parser.add_argument('--heartbeat-interval', type=int, default=DEFAULT_HEARTBEAT_INTERVAL,
                        help='Device heartbeat interval in seconds')
    parser.add_argument('--authorize-request-msat', type=int, default=DEFAULT_AUTHORIZE_REQUEST_MSAT,
                        help='Device authorization request amount in millisatoshis')
    parser.add_argument('--device-timestamp', default=None,
                        help='RFC3339 timestamp for the provisioned device; defaults to current UTC time')

    parser.add_argument('--results-root', default=DEFAULT_RESULTS_ROOT,
                        help='Directory that receives per-run output directories')
    parser.add_argument('--run-name', default=None,
                        help='Run directory name; defaults to UTC timestamp plus target host')
    parser.add_argument('--dry-run', action='store_true',
                        help='Print planned commands without executing them')

    return parser.parse_args()


def main() -> int:
    args = parse_args()
    project_root = Path(__file__).resolve().parents[2]
    run_name = sanitize_name(args.run_name or default_run_name(args.target_host))
    results_root = path_from_project(project_root, args.results_root)
    run_dir = results_root / run_name
    manifest_path = run_dir / 'experiment.json'
    override_file = run_dir / 'external-target.override.yml'
    device_response_path = run_dir / 'device_response.json'

    local_compose_file = path_from_project(project_root, args.local_compose_file)
    api_base_url = args.api_base_url or f"http://{host_for_url(args.target_host)}:{args.api_port}"
    device_url = join_url(api_base_url, args.devices_endpoint)
    mqtt_tls_server_name = args.mqtt_tls_server_name or args.target_host
    should_reset_target_data = not args.skip_target_restart and not args.skip_target_data_reset
    target_data_dir = validate_remote_data_dir(args.target_data_dir) if should_reset_target_data else None
    device_payload = build_device_payload(args)

    manifest = {
        'run_name': run_name,
        'created_at': iso_z(utc_now()),
        'target': {
            'host': args.target_host,
            'user': args.target_user,
            'ssh_destination': ssh_destination(args.target_user, args.target_host),
            'dir': args.target_dir,
            'compose_file': args.target_compose_file,
            'data_dir': target_data_dir,
            'data_reset_enabled': should_reset_target_data,
            'data_reset_sudo': should_reset_target_data and not args.no_target_data_reset_sudo,
            'api_base_url': api_base_url,
        },
        'local': {
            'project_root': str(project_root),
            'compose_file': str(local_compose_file),
            'compose_override_file': str(override_file),
            'mqtt_tls_server_name': mqtt_tls_server_name,
            'smartmeter_url': 'http://localhost:3010',
            'grafana_url': 'http://localhost:3000',
            'prometheus_url': 'http://localhost:9090',
        },
        'device': {
            'url': device_url,
            'request': device_payload,
            'response_file': str(device_response_path),
            'status_code': None,
            'attempts': None,
        },
        'outputs': {
            'run_dir': str(run_dir),
            'manifest': str(manifest_path),
            'device_response': str(device_response_path),
            'target_diagnostics': str(run_dir / 'target_diagnostics.log'),
        },
        'commands': {},
        'status': 'running' if not args.dry_run else 'dry-run',
    }

    run_dir.mkdir(parents=True, exist_ok=True)
    write_functional_compose_override(
        override_file,
        target_host=args.target_host,
        mqtt_tls_server_name=mqtt_tls_server_name,
        device_id=args.device_id,
        device_secret=args.device_secret,
    )
    write_json(manifest_path, manifest)

    print(f"Run directory: {run_dir}")
    print(f"Target API: {api_base_url}")
    print(f"Device endpoint: {device_url}")

    try:
        destination = ssh_destination(args.target_user, args.target_host)
        if not args.skip_target_restart:
            down_cmd = remote_compose_command(
                destination,
                args.target_dir,
                args.target_compose_file,
                ['down', '-v', '--remove-orphans'],
            )
            pull_cmd = remote_compose_command(destination, args.target_dir, args.target_compose_file, ['pull'])
            up_cmd = remote_compose_command(
                destination,
                args.target_dir,
                args.target_compose_file,
                ['up', '-d', '--remove-orphans'],
            )
            manifest['commands']['target_down'] = command_text(down_cmd)
            reset_data_cmd = None
            if target_data_dir is not None:
                reset_data_cmd = remote_reset_data_command(
                    destination,
                    args.target_dir,
                    target_data_dir,
                    use_sudo=not args.no_target_data_reset_sudo,
                )
                manifest['commands']['target_data_reset'] = command_text(reset_data_cmd)
            if args.pull_target_images:
                manifest['commands']['target_pull'] = command_text(pull_cmd)
            manifest['commands']['target_up'] = command_text(up_cmd)
            write_json(manifest_path, manifest)
            run_command(down_cmd, cwd=project_root, dry_run=args.dry_run)
            if reset_data_cmd is not None:
                run_command(reset_data_cmd, cwd=project_root, dry_run=args.dry_run)
                manifest['target']['data_reset_at'] = iso_z(utc_now())
                write_json(manifest_path, manifest)
            if args.pull_target_images:
                run_command(pull_cmd, cwd=project_root, dry_run=args.dry_run)
            try:
                run_command(up_cmd, cwd=project_root, dry_run=args.dry_run)
            except subprocess.CalledProcessError:
                if not args.dry_run:
                    collect_target_diagnostics(args, project_root, run_dir)
                raise
            manifest['target']['restarted_at'] = iso_z(utc_now())
            write_json(manifest_path, manifest)
            sleep_with_progress(args.target_stabilize_seconds, 'target edge stack stabilization', args.dry_run)

        if not args.skip_local_restart:
            down_cmd = local_compose_command(project_root, local_compose_file, override_file, ['down', '-v', '--remove-orphans'])
            up_cmd = local_compose_command(project_root, local_compose_file, override_file, ['up', '-d', '--remove-orphans'])
            manifest['commands']['local_down'] = command_text(down_cmd)
            manifest['commands']['local_up'] = command_text(up_cmd)
            write_json(manifest_path, manifest)
            run_command(down_cmd, cwd=project_root, dry_run=args.dry_run)
            run_command(up_cmd, cwd=project_root, dry_run=args.dry_run)
            manifest['local']['restarted_at'] = iso_z(utc_now())
            write_json(manifest_path, manifest)
            sleep_with_progress(args.local_stabilize_seconds, 'local evaluation stack stabilization', args.dry_run)

        manifest['commands']['provision_device'] = f"POST {device_url}"
        write_json(manifest_path, manifest)
        device_result = provision_device(
            url=device_url,
            payload=device_payload,
            response_path=device_response_path,
            dry_run=args.dry_run,
            request_timeout_seconds=args.request_timeout_seconds,
            ready_timeout_seconds=args.api_ready_timeout_seconds,
            retry_interval_seconds=args.api_retry_interval_seconds,
        )
        manifest['device'].update(device_result)
        manifest['status'] = 'completed' if not args.dry_run else 'dry-run'
        write_json(manifest_path, manifest)

        print(f"Experiment manifest: {manifest_path}")
        print("Local evaluation URLs:")
        print("  Smart Meter UI: http://localhost:3010")
        print("  Grafana: http://localhost:3000")
        print("  Prometheus: http://localhost:9090")
        return 0

    except KeyboardInterrupt:
        manifest['status'] = 'interrupted'
        manifest['interrupted_at'] = iso_z(utc_now())
        write_json(manifest_path, manifest)
        print('\nInterrupted. Manifest updated with the partial run state.', file=sys.stderr)
        return 130
    except Exception as exc:
        manifest['status'] = 'failed'
        manifest['failed_at'] = iso_z(utc_now())
        manifest['error'] = str(exc)
        write_json(manifest_path, manifest)
        print(f"Experiment failed: {exc}", file=sys.stderr)
        return 1


if __name__ == '__main__':
    sys.exit(main())
