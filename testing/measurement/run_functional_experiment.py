#!/usr/bin/env python3
"""
Orchestrate a LINA edge functional experiment.

The runner starts from a clean edge stack, starts the local evaluation stack,
and provisions one smart-meter device on the target API.

Example:
  python3 testing/measurement/run_functional_experiment.py --target-host 192.168.0.200

Use --dry-run to inspect the SSH, Docker Compose, remote data reset, and device
provisioning commands without executing them.
"""

from __future__ import annotations

